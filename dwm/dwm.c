void
movemouse(const Arg *arg)
{
	int x, y, ocx, ocy, nx, ny;
	Client *c;
	Monitor *m;
	XEvent ev;
	Time lasttime = 0;

	if (!(c = selmon->sel))
		return;
	if (c->isfullscreen) /* no support moving fullscreen windows by mouse */
		return;
	restack(selmon);
	ocx = c->x;
	ocy = c->y;
	if (XGrabPointer(dpy, root, False, MOUSEMASK, GrabModeAsync, GrabModeAsync,
		None, cursor[CurMove]->cursor, CurrentTime) != GrabSuccess)
		return;
	if (!getrootptr(&x, &y))
		return;
	do {
		XMaskEvent(dpy, MOUSEMASK|ExposureMask|SubstructureRedirectMask, &ev);
		switch(ev.type) {
		case ConfigureRequest:
		case Expose:
		case MapRequest:
			handler[ev.type](&ev);
			break;
		case MotionNotify:
			if ((ev.xmotion.time - lasttime) <= (1000 / refreshrate))
				continue;
			lasttime = ev.xmotion.time;

			nx = ocx + (ev.xmotion.x - x);
			ny = ocy + (ev.xmotion.y - y);
			if (abs(selmon->wx - nx) < snap)
				nx = selmon->wx;
			else if (abs((selmon->wx + selmon->ww) - (nx + WIDTH(c))) < snap)
				nx = selmon->wx + selmon->ww - WIDTH(c);
			if (abs(selmon->wy - ny) < snap)
				ny = selmon->wy;
			else if (abs((selmon->wy + selmon->wh) - (ny + HEIGHT(c))) < snap)
				ny = selmon->wy + selmon->wh - HEIGHT(c);
			if (!c->isfloating && selmon->lt[selmon->sellt]->arrange
			&& (abs(nx - c->x) > snap || abs(ny - c->y) > snap))
				togglefloating(NULL);
			if (!selmon->lt[selmon->sellt]->arrange || c->isfloating)
				resize(c, nx, ny, c->w, c->h, 1);
			break;
		}
	} while (ev.type != ButtonRelease);
	XUngrabPointer(dpy, CurrentTime);
	if ((m = recttomon(c->x, c->y, c->w, c->h)) != selmon) {
		sendmon(c, m);
		selmon = m;
		focus(NULL);
	}
}

unsigned int
nbclienttag(Client *c)
{
	unsigned int i, mask;

	mask = c->tags & TAGMASK;
	for (i = 0; i < LENGTH(tags); i++)
		if (mask & (1 << i))
			return 1 << i;
	return selmon ? selmon->tagset[selmon->seltags] & TAGMASK : 1;
}

unsigned int
nbtiledcount(Monitor *m, unsigned int tagmask)
{
	Client *c;
	unsigned int n = 0;

	for (c = m->clients; c; c = c->next)
		if (!c->isfloating && (c->tags & tagmask))
			n++;
	return n;
}

void
nbplaceoverflow(Client *c)
{
	unsigned int current, i, idx, tagmask;

	if (!c || c->isfloating)
		return;
	current = nbclienttag(c);
	if (nbtiledcount(c->mon, current) < 4)
		return;
	for (idx = 0; idx < LENGTH(tags); idx++)
		if (current & (1 << idx))
			break;
	if (idx >= LENGTH(tags))
		idx = 0;
	for (i = 1; i < LENGTH(tags); i++) {
		tagmask = 1 << ((idx + i) % LENGTH(tags));
		if (nbtiledcount(c->mon, tagmask) < 4) {
			c->tags = tagmask;
			return;
		}
	}
}

void
nbresizefocused(int action)
{
	Client *c;
	Monitor *m;
	int cx, cy, splitx, splity, step = 5;

	if (!selmon || !(c = selmon->sel) || c->isfloating || c->isfullscreen)
		return;
	m = c->mon;
	if (!m->lt[m->sellt]->arrange)
		return;

	cx = c->x + WIDTH(c) / 2;
	cy = c->y + HEIGHT(c) / 2;
	splitx = m->wx + (m->ww * m->nbsplitx) / 100;
	splity = m->wy + (m->wh * m->nbsplity) / 100;

	switch (action) {
	case NB_RESIZE_WIDER:
		m->nbsplitx += cx < splitx ? step : -step;
		break;
	case NB_RESIZE_NARROWER:
		m->nbsplitx += cx < splitx ? -step : step;
		break;
	case NB_RESIZE_TALLER:
		m->nbsplity += cy < splity ? step : -step;
		break;
	case NB_RESIZE_SHORTER:
		m->nbsplity += cy < splity ? -step : step;
		break;
	}

	if (m->nbsplitx < 25)
		m->nbsplitx = 25;
	else if (m->nbsplitx > 75)
		m->nbsplitx = 75;
	if (m->nbsplity < 25)
		m->nbsplity = 25;
	else if (m->nbsplity > 75)
		m->nbsplity = 75;
	arrange(m);
}

int
nbslotoverlaps(Monitor *m, int a, int b)
{
	int ax, ay, aw, ah, bx, by, bw, bh;

	if (!nbslotrect(m, a, &ax, &ay, &aw, &ah)
	|| !nbslotrect(m, b, &bx, &by, &bw, &bh))
		return 1;
	return ax < bx + bw && ax + aw > bx && ay < by + bh && ay + ah > by;
}

int
nbslotrect(Monitor *m, int slot, int *x, int *y, int *w, int *h)
{
	int hw = (m->ww * m->nbsplitx) / 100;
	int hh = (m->wh * m->nbsplity) / 100;

	*x = m->wx;
	*y = m->wy;
	*w = m->ww;
	*h = m->wh;
	switch (slot) {
	case NB_SLOT_LEFT:
		*w = hw;
		break;
	case NB_SLOT_RIGHT:
		*x = m->wx + hw;
		*w = m->ww - hw;
		break;
	case NB_SLOT_TOP:
		*h = hh;
		break;
	case NB_SLOT_BOTTOM:
		*y = m->wy + hh;
		*h = m->wh - hh;
		break;
	case NB_SLOT_TOPLEFT:
		*w = hw;
		*h = hh;
		break;
	case NB_SLOT_TOPRIGHT:
		*x = m->wx + hw;
		*w = m->ww - hw;
		*h = hh;
		break;
	case NB_SLOT_BOTTOMLEFT:
		*y = m->wy + hh;
		*w = hw;
		*h = m->wh - hh;
		break;
	case NB_SLOT_BOTTOMRIGHT:
		*x = m->wx + hw;
		*y = m->wy + hh;
		*w = m->ww - hw;
		*h = m->wh - hh;
		break;
	case NB_SLOT_AUTO:
		break;
	default:
		return 0;
	}
	return 1;
}

