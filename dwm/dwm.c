void
recordtaghistory(unsigned int tagmask)
{
	unsigned int i, j;

	tagmask &= TAGMASK;
	if (!tagmask)
		return;
	cycletagactive = 0;
	if (taghistory[0] == tagmask)
		return;
	for (i = 0; i < LENGTH(tags); i++)
		if (taghistory[i] == tagmask)
			break;
	for (j = (i < LENGTH(tags) ? i : LENGTH(tags) - 1); j > 0; j--)
		taghistory[j] = taghistory[j - 1];
	taghistory[0] = tagmask;
}

void
setviewtag(unsigned int tagmask, int record)
{
	tagmask &= TAGMASK;
	if (!tagmask || tagmask == selmon->tagset[selmon->seltags])
		return;
	selmon->seltags ^= 1;
	selmon->tagset[selmon->seltags] = tagmask;
	if (record)
		recordtaghistory(tagmask);
	focus(NULL);
	arrange(selmon);
}

void
cycletag(const Arg *arg)
{
	unsigned int current, occ, used = 0, i, mask;
	int dir = arg->i < 0 ? -1 : 1;

	current = selmon->tagset[selmon->seltags] & TAGMASK;
	occ = occupiedtags(selmon);
	if (!occ)
		return;

	if (!cycletagactive) {
		cycletagcount = 0;
		cycletagpos = 0;
		for (i = 0; i < LENGTH(tags); i++) {
			mask = taghistory[i] & occ & TAGMASK;
			if (mask && mask != current && !(used & mask)) {
				cycletags[cycletagcount++] = mask;
				used |= mask;
			}
		}
		for (i = LENGTH(tags); i > 0; i--) {
			mask = 1 << (i - 1);
			if ((occ & mask) && mask != current && !(used & mask)) {
				cycletags[cycletagcount++] = mask;
				used |= mask;
			}
		}
		if ((occ & current) && !(used & current))
			cycletags[cycletagcount++] = current;
		cycletagactive = cycletagcount > 0;
	}
	if (!cycletagactive || !cycletagcount)
		return;

	for (i = 0; i < cycletagcount; i++) {
		if (dir < 0)
			cycletagpos = (cycletagpos + cycletagcount - 1) % cycletagcount;
		mask = cycletags[cycletagpos] & occupiedtags(selmon);
		if (dir > 0)
			cycletagpos = (cycletagpos + 1) % cycletagcount;
		if (mask) {
			setviewtag(mask, 0);
			return;
		}
	}
	cycletagactive = 0;
}

