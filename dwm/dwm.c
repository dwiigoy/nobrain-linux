void
keypress(XEvent *e)
{
	unsigned int i;
	KeySym keysym;
	XKeyEvent *ev;

	ev = &e->xkey;
	keysym = XKeycodeToKeysym(dpy, (KeyCode)ev->keycode, 0);
	if (selmon && selmon->nbresizemode) {
		switch (keysym) {
		case XK_Right:
		case XK_KP_Right:
			nbresizefocused(NB_RESIZE_WIDER);
			return;
		case XK_Left:
		case XK_KP_Left:
			nbresizefocused(NB_RESIZE_NARROWER);
			return;
		case XK_Down:
		case XK_KP_Down:
			nbresizefocused(NB_RESIZE_TALLER);
			return;
		case XK_Up:
		case XK_KP_Up:
			nbresizefocused(NB_RESIZE_SHORTER);
			return;
		case XK_Escape:
		case XK_Return:
		case XK_KP_Enter:
			selmon->nbresizemode = 0;
			grabkeys();
			drawbar(selmon);
			return;
		}
	}
	for (i = 0; i < LENGTH(keys); i++)
		if (keysym == keys[i].keysym
		&& ((keys[i].func == setnbslot
			&& (CLEANMASK(ev->state) & ControlMask)
			&& (CLEANMASK(ev->state) & (Mod1Mask|Mod3Mask|Mod4Mask|Mod5Mask))
			&& !(CLEANMASK(ev->state) & ShiftMask))
			|| (keys[i].func != setnbslot
			&& CLEANMASK(keys[i].mod) == CLEANMASK(ev->state)))
		&& keys[i].func)
			keys[i].func(&(keys[i].arg));
}

