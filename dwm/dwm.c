void
checkkbreserve(void)
{
	FILE *f;
	int val = 0;

	f = fopen("/tmp/.nobrain_kb_reserve", "r");
	if (f) {
		if (fscanf(f, "%d", &val) != 1)
			val = 0;
		fclose(f);
	}
	if (val < 0)
		val = 0;
	if (val != kbreserve) {
		kbreserve = val;
		applykbreserve();
	}
}

void
run(void)
{
	XEvent ev;
	struct timeval tv;
	fd_set fds;
	int xfd = ConnectionNumber(dpy);

	/* main event loop */
	XSync(dpy, False);
	while (running) {
		while (XPending(dpy)) {
			XNextEvent(dpy, &ev);
			if (handler[ev.type])
				handler[ev.type](&ev); /* call handler */
			if (!running)
				return;
		}
		FD_ZERO(&fds);
		FD_SET(xfd, &fds);
		tv.tv_sec = 0;
		tv.tv_usec = 200000;
		select(xfd + 1, &fds, NULL, NULL, &tv);
		checkkbreserve();
	}
}

void
scan(void)
{
	unsigned int i, num;
	Window d1, d2, *wins = NULL;
	XWindowAttributes wa;

	if (XQueryTree(dpy, root, &d1, &d2, &wins, &num)) {
		for (i = 0; i < num; i++) {
			if (!XGetWindowAttributes(dpy, wins[i], &wa)
			|| wa.override_redirect || XGetTransientForHint(dpy, wins[i], &d1))
				continue;
			if (wa.map_state == IsViewable || getstate(wins[i]) == IconicState)
				manage(wins[i], &wa);
		}
		for (i = 0; i < num; i++) { /* now the transients */
			if (!XGetWindowAttributes(dpy, wins[i], &wa))
				continue;
			if (XGetTransientForHint(dpy, wins[i], &d1)
			&& (wa.map_state == IsViewable || getstate(wins[i]) == IconicState))
				manage(wins[i], &wa);
		}
		if (wins)
			XFree(wins);
	}
}

