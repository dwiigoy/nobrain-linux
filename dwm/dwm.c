void
tile(Monitor *m)
{
	static const int pref2land[] = { NB_SLOT_LEFT, NB_SLOT_RIGHT };
	static const int pref2port[] = { NB_SLOT_TOP, NB_SLOT_BOTTOM };
	static const int pref3land[] = { NB_SLOT_LEFT, NB_SLOT_TOPRIGHT, NB_SLOT_BOTTOMRIGHT };
	static const int pref3port[] = { NB_SLOT_TOP, NB_SLOT_BOTTOMLEFT, NB_SLOT_BOTTOMRIGHT };
	static const int pref4[] = { NB_SLOT_TOPLEFT, NB_SLOT_TOPRIGHT, NB_SLOT_BOTTOMLEFT, NB_SLOT_BOTTOMRIGHT };
	static const int fallback[] = {
		NB_SLOT_TOPLEFT, NB_SLOT_TOPRIGHT, NB_SLOT_BOTTOMLEFT, NB_SLOT_BOTTOMRIGHT,
		NB_SLOT_LEFT, NB_SLOT_RIGHT, NB_SLOT_TOP, NB_SLOT_BOTTOM
	};
	Client *clients[32];
	Client *c;
	const int *pref;
	unsigned int i, j, n, preflen, usedn;
	int cand, ok, x, y, w, h;
	int slots[32], used[32];

	for (n = 0, c = nexttiled(m->clients); c && n < LENGTH(clients); c = nexttiled(c->next), n++)
		clients[n] = c;
	if (n == 0)
		return;
	for (i = 0; i < n / 2; i++) {
		c = clients[i];
		clients[i] = clients[n - 1 - i];
		clients[n - 1 - i] = c;
	}
	for (i = 0; i < n; i++)
		slots[i] = -1;

	usedn = 0;
	for (i = 0; i < n; i++) {
		cand = clients[i]->nbslot;
		if (cand == NB_SLOT_AUTO)
			continue;
		ok = nbslotrect(m, cand, &x, &y, &w, &h);
		for (j = 0; ok && j < usedn; j++)
			if (nbslotoverlaps(m, cand, used[j]))
				ok = 0;
		if (ok) {
			slots[i] = cand;
			used[usedn++] = cand;
		}
	}

	if (n == 1 && usedn == 0)
		slots[0] = NB_SLOT_AUTO;
	else {
		if (n == 2) {
			pref = m->ww >= m->wh ? pref2land : pref2port;
			preflen = LENGTH(pref2land);
		} else if (n == 3) {
			pref = m->ww >= m->wh ? pref3land : pref3port;
			preflen = LENGTH(pref3land);
		} else {
			pref = pref4;
			preflen = LENGTH(pref4);
		}
		for (i = 0; i < n; i++) {
			if (slots[i] != -1)
				continue;
			for (j = 0; j < preflen && slots[i] == -1; j++) {
				cand = pref[j];
				ok = 1;
				for (unsigned int k = 0; ok && k < usedn; k++)
					if (nbslotoverlaps(m, cand, used[k]))
						ok = 0;
				if (ok) {
					slots[i] = cand;
					used[usedn++] = cand;
				}
			}
			for (j = 0; j < LENGTH(fallback) && slots[i] == -1; j++) {
				cand = fallback[j];
				ok = 1;
				for (unsigned int k = 0; ok && k < usedn; k++)
					if (nbslotoverlaps(m, cand, used[k]))
						ok = 0;
				if (ok) {
					slots[i] = cand;
					used[usedn++] = cand;
				}
			}
			if (slots[i] == -1)
				slots[i] = NB_SLOT_AUTO;
		}
	}

	for (i = 0; i < n; i++) {
		c = clients[i];
		nbslotrect(m, slots[i], &x, &y, &w, &h);
		resize(c, x, y, MAX(1, w - 2 * c->bw), MAX(1, h - 2 * c->bw), 0);
	}
}

